package chapter03;

public class Operator10 {

	public static void main(String[] args) {

		String str1 = "JAVA";
		String str2 = "programing";
		
		//String은 참조변수로써 참조변수는 객체를 가르키는 메모리 주소를 가지고있는거다.
		//참조변수의 관계연산은 ==,!= 두개만 이루어진다.
		System.out.println(str1 == str2);
		System.out.println(str1 != str2);
		
		
	}

}
